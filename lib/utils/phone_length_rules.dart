/// National mobile number length rules per ISO country code.
/// Derived from libphonenumber metadata; Colombia enforces exact 10 digits starting with 3.
class PhoneLengthRule {
  const PhoneLengthRule({
    required this.min,
    required this.max,
    this.exact = false,
    this.pattern,
  });

  final int min;
  final int max;
  final bool exact;
  final RegExp? pattern;

  bool isValidLength(int length) {
    if (exact) return length == min;
    return length >= min && length <= max;
  }

  bool matchesPattern(String digits) {
    if (pattern == null) return true;
    return pattern!.hasMatch(digits);
  }
}

