PhoneLengthRule phoneLengthRuleForDialCode(String? dialCode, {String? isoCode}) {
  if (isoCode != null && isoCode.isNotEmpty) {
    final byIso = _rulesByIso[isoCode.toUpperCase()];
    if (byIso != null) return byIso;
  }
  final digits = dialCode?.replaceAll(RegExp(r'\D'), '') ?? '';
  if (digits.isEmpty) return _rulesByIso['CO'] ?? _defaultRule;
  return _rulesByDialCode[digits] ?? _defaultRule;
}

